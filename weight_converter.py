def gram_to_kilogram(gram):
    return gram / 1000

def kilogram_to_gram(kilogram):
    return kilogram * 1000

def kilogram_to_pound(kilogram):
    return kilogram * 2.20462

def pound_to_kilogram(pound):
    return pound / 2.20462

def gram_to_pound(gram):
    return gram / 453.592

def pound_to_gram(pound):
    return pound * 453.592